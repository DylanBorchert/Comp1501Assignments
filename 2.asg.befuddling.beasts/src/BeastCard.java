class BeastCard {

    private String name;
    private String cost;

    public BeastCard(String name, String cost) {
	this.name = name;
	this.cost = cost.toUpperCase();
    }

    //This method returns the name of this card.
    public String name() {
	return name;
    }

    //This method returns the cost of this card.
    public String cost() {
	return cost;

    }

    //This toString displays when a card is drawn
    @Override
    public String toString() {
	return name + " (" + cost + ")";
    }

}
